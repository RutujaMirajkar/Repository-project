package TestPackage1;

public class incredecrement {
	public static void main(String[] args) {
		
		int a=60;
		
		System.out.println(a);
		
		
		System.out.println(a++); //value 60 but store 61
		
		
		System.out.println(a);   // now added value 61
		
		
		
		System.out.println(--a); //60 value after substracting
		
		
	}

}
